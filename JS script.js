window.onload = () => {
    initLang();
    initParticles();
    // Re-apply translations on load
    applyTranslates(localStorage.getItem('agri_lang') || 'en');
};

function initLang() {
    const langSelect = document.getElementById('lang-selector');
    if (!langSelect) return;
    
    let savedLang = localStorage.getItem('agri_lang') || 'en';
    langSelect.value = savedLang;
    
    langSelect.addEventListener('change', (e) => {
        localStorage.setItem('agri_lang', e.target.value);
        applyTranslates(e.target.value);
    });
}

function applyTranslates(lang) {
    const elements = document.querySelectorAll('[data-i18n]');
    elements.forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (translations[lang] && translations[lang][key]) {
            if (el.tagName === 'INPUT' && el.type === 'submit') {
                el.value = translations[lang][key];
            } else if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
                el.placeholder = translations[lang][key];
            } else {
                el.innerText = translations[lang][key];
            }
        }
    });
}

/** 
 * Handles File Uploads globally
 */
function handleFileUpload(inputId, previewId) {
    const fileInput = document.getElementById(inputId);
    const previewContainer = document.getElementById(previewId);
    
    fileInput.addEventListener('change', function(e) {
        const file = e.target.files[0];
        if (file) {
            const url = URL.createObjectURL(file);
            previewContainer.innerHTML = `<img src="${url}" alt="Preview" style="max-width: 100%; max-height: 200px; border-radius: 8px;">`;
        }
    });
}

function clearFileUpload(inputId, previewId) {
    document.getElementById(inputId).value = "";
    document.getElementById(previewId).innerHTML = "";
}

// Ensure smooth navigation highlighting
document.addEventListener('DOMContentLoaded', () => {
    const links = document.querySelectorAll('nav a');
    links.forEach(link => {
        if (link.href === window.location.href) {
            link.classList.add('active');
        }
    });
});
